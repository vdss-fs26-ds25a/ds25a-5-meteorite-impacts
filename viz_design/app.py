def server(input, output, session):
    # Defaults
    cur_years = reactive.Value([1950, 2024])
    cur_mass_log = reactive.Value([MASS_LOG_MIN, MASS_LOG_MAX])
    cur_limit = reactive.Value(1000)
    cur_fall_filter = reactive.Value(["Fell", "Found"])
    cur_sampling_mode = reactive.Value("hybrid")

    # Trace Toggles
    show_impacts = reactive.Value(True)
    show_heatmap = reactive.Value(False)

    @reactive.calc
    def filtered_data():
        if meteorites.empty:
            return pd.DataFrame()
        try:
            years = input.year_range()
            mass_log = input.mass_range_log()
            limit = input.limit()
            fall_types = input.fall_filter()
            sampling_mode = input.sampling_mode()
        except (TypeError, ValueError, RuntimeError):
            years = cur_years()
            mass_log = cur_mass_log()
            limit = cur_limit()
            fall_types = cur_fall_filter()
            sampling_mode = cur_sampling_mode()
        return filter_and_sample(
            meteorites,
            years=years,
            mass_log=mass_log,
            fall_types=fall_types,
            limit=limit,
            sampling_mode=sampling_mode,
            seed=42,
        )

    @output
    @render.ui
    def menu_container():
        return ui.div(
            ui.h3("ORBITAL DATA"),
            ui.div(ui.output_text("count_summary"), class_="impact-count"),
            ui.hr(style="border-color: #333; margin: 15px 0;"),
            ui.input_switch("show_impacts", "Show Impacts",
                            value=show_impacts()),
            ui.input_switch("show_heatmap", "Show 3D Heatmap",
                            value=show_heatmap()),
            ui.hr(style="border-color: #333; margin: 15px 0;"),
            ui.input_checkbox_group(
                "fall_filter", "FALL TYPE",
                {"Fell": "Fallen", "Found": "Found"},
                selected=cur_fall_filter(),
                inline=True
            ),
            ui.input_slider("year_range", "YEARS",
                            min=int(meteorites['year'].min()),
                            max=int(meteorites['year'].max()),
                            value=cur_years(),
                            sep=""),
            ui.input_slider("mass_range_log", "MASS (log)",
                            min=MASS_LOG_MIN, max=MASS_LOG_MAX, value=cur_mass_log(), step=0.1),
            ui.div(ui.output_text("mass_range_label"),
                   style="font-size: 12px; color: #bbb; margin-top: 6px;"),
            ui.input_select(
                "sampling_mode", "SAMPLING MODE",
                {
                    "hybrid": "Hybrid (recommended)",
                    "random": "Random",
                    "top_mass": "Top mass"
                },
                selected=cur_sampling_mode()
            ),
            ui.input_numeric("limit", "MAX POINTS",
                             value=cur_limit(), min=1, max=45000),
            class_="menu-widget"
        )

    @render.text
    def count_summary():
        df = filtered_data()
        return f"{len(df):,} DETECTED IMPACTS"

    @render.text
    def mass_range_label():
        try:
            mass_log = input.mass_range_log()
        except (TypeError, ValueError, RuntimeError):
            mass_log = cur_mass_log()
        low_g = 10 ** mass_log[0]
        high_g = 10 ** mass_log[1]
        return f"Selected mass: {format_mass(low_g)} to {format_mass(high_g)}"

    @render_widget
    def globe_plot():
        df = filtered_data()
        try:
            s_heatmap = input.show_heatmap()
        except (TypeError, ValueError, RuntimeError):
            s_heatmap = show_heatmap()
        try:
            s_impacts = input.show_impacts()
        except (TypeError, ValueError, RuntimeError):
            s_impacts = show_impacts()
        try:
            mass_log = input.mass_range_log()
        except (TypeError, ValueError, RuntimeError):
            mass_log = cur_mass_log()
        return build_globe_plot(
            df,
            mass_log=mass_log,
            show_impacts=s_impacts,
            show_heatmap=s_heatmap,
        )

    @render_widget
    def heatmap_2d():
        df = filtered_data()
        return build_heatmap_2d(df)

    @render_widget
    def timeline_plot():
        df = filtered_data()
        return build_timeline_plot(df)

    @render_widget
    def mass_hist():
        df = filtered_data()
        return build_mass_hist(df)

    @render_widget
    def class_bar():
        df = filtered_data()
        return build_class_bar(df)


app = App(app_ui, server)
