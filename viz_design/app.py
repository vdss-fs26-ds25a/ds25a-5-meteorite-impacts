# Load cleaned data generated by data/data.py
def load_cleaned_data():
    data_rel_path = Path("data/processed/Meteorite_Landings_cleaned.csv")
    candidate_paths = [
        Path(__file__).parent / data_rel_path,
        Path(__file__).parent.parent / data_rel_path,
    ]
    for data_path in candidate_paths:
        try:
            df = pd.read_csv(data_path)
            df["log_mass"] = np.log10(df["mass_g"])
            return df
        except Exception:
            continue
    print(f"Error: could not load cleaned data from any of {candidate_paths}")
    return pd.DataFrame()


